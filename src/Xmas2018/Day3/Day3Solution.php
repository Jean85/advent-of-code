    public function solve()
    {
        $conflictingClaims = 0;

        foreach ($this->fabric->iterate() as $squareInch) {
            if ($squareInch->getClaimCount() > 1) {
                ++$conflictingClaims;
            }
        }

        return $conflictingClaims;
    }

    public function solveSecondPart()
    {
        $uncontestedClaims = [];
        foreach ($this->claims as $claim) {
            $uncontestedClaims[$claim->getId()] = true;
        }

        foreach ($this->fabric->iterate() as $squareInch) {
            if ($squareInch->getClaimCount() > 1) {
                foreach ($squareInch->getClaims() as $claim) {
                    unset($uncontestedClaims[$claim->getId()]);
                }
            }
        }

        return implode(', ', array_keys($uncontestedClaims));
    }

